#include <unistd.h>

void	ft_print_comb(void);
void	ft_print(char i, char j, char k);

void	ft_print_comb(void)
{
	char	i;
	char	j;
	char	k;

	i = '0';
	while (i <= '7')
	{
		j = i + 1;
		while (j <= '8')
		{
			k = j + 1;
			while (k <= '9')
			{
				ft_print(i, j, k);
				k++;
				if (i < '7')
				{
					write(1, ", ", 2);
				}
			}
		j++;
		}
	i++;
	}
}

void	ft_print(char i, char j, char k)
{
	write(1, &i, 1);
	write(1, &j, 1);
	write(1, &k, 1);
}
